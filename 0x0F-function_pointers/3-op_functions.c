#include "3-calc.h"

/**
 * op_add - int1+ int2
 *
 * @a: int1
 * @b: int2
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - int1 - int2
 *
 * @a: int1
 * @b: int2
 *
 * Return: the result of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - int1*int2
 *
 * @a: int1
 * @b: int2
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - int1/int2
 *
 * @a: int1
 * @b: int2
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - int1%int2
 *
 * @a: int2
 * @b: int2
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
